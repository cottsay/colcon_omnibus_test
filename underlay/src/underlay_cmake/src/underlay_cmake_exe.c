#include <errno.h>
#include <stdio.h>
#include <stdlib.h>

#include "underlay_cmake/underlay_cmake.h"

int main(int argc, char * argv[])
{
  (void)argc;
  (void)argv;

  const char ** res = underlay_cmake_func();
  if (NULL == res) {
    return ENOMEM;
  }

  for (const char ** i = res; NULL != *i; i++) {
    printf("%s\n", *i);
  }

  underlay_cmake_free(res);

  return 0;
}
